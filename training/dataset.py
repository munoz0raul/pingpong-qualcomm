#!/usr/bin/env python3
"""
PyTorch Dataset for Phase A — reads the .npy cache generated by preprocess.py.

Each returned item is a pair (image, target):
  image  : float32 tensor (3, 320, 320) with values in [0, 1], RGB channels.
  target : float32 tensor (5,) = [obj, cx, cy, w, h] normalized (0..1).

Why (C, H, W) and not (H, W, C)?  PyTorch convention: convolutions expect the
channel first. The cache stores (H, W, C) uint8 (natural image format), and
here we convert to (C, H, W) float and divide by 255.

The cache is opened with mmap_mode="r": the arrays are NOT loaded whole into
RAM: the OS brings the page from disk on demand. Good for not blowing up memory
and still being fast.
"""

import os

import numpy as np
import torch
from torch.utils.data import Dataset

_THIS_DIR = os.path.dirname(os.path.abspath(__file__))
CACHE_DIR = os.path.join(_THIS_DIR, "cache")

IMG_SIZE = 320
TARGET_DIM = 5


class PaddleDataset(Dataset):
    """1-box detection dataset. split = 'training' or 'testing'."""

    def __init__(self, split):
        img_path = os.path.join(CACHE_DIR, f"{split}_images.npy")
        tgt_path = os.path.join(CACHE_DIR, f"{split}_targets.npy")
        if not (os.path.exists(img_path) and os.path.exists(tgt_path)):
            raise FileNotFoundError(
                f"cache for '{split}' not found — run preprocess.py first"
            )
        self.images = np.load(img_path, mmap_mode="r")   # (N, 320, 320, 3) uint8
        self.targets = np.load(tgt_path, mmap_mode="r")  # (N, 5) float32
        self.split = split

    def __len__(self):
        return len(self.images)

    def __getitem__(self, idx):
        img = self.images[idx]                       # (320,320,3) uint8, RGB
        # HWC uint8 -> CHW float32 in [0,1]
        t = torch.from_numpy(np.ascontiguousarray(img)).float().div_(255.0)
        t = t.permute(2, 0, 1).contiguous()          # (3,320,320)
        target = torch.from_numpy(np.array(self.targets[idx], dtype=np.float32))
        return t, target

    def pos_weight(self):
        """
        Weight for the 'obj' loss (BCEWithLogits). Compensates the imbalance
        between images with paddle and background: pos_weight = n_neg / n_pos.
        """
        obj = np.asarray(self.targets[:, 0])
        n_pos = float((obj > 0.5).sum())
        n_neg = float((obj <= 0.5).sum())
        if n_pos == 0:
            return torch.tensor(1.0)
        return torch.tensor(n_neg / n_pos, dtype=torch.float32)


if __name__ == "__main__":
    # Sanity check: show shapes and the target of a positive sample.
    ds = PaddleDataset("training")
    print(f"training: {len(ds)} samples | pos_weight={ds.pos_weight():.2f}")
    img, tgt = ds[0]
    print(f"img shape {tuple(img.shape)} dtype {img.dtype} range [{img.min():.2f},{img.max():.2f}]")
    print(f"target[0] {tgt.numpy()}")
    # first sample with a paddle
    for i in range(len(ds)):
        _, t = ds[i]
        if t[0] > 0.5:
            print(f"positive sample idx={i}: [obj,cx,cy,w,h]={t.numpy()}")
            break
